# OOPs - To map with real world scenarios, we started using objects in code . That is called object Oriented Programming .
# Class - blueprint for creating objects

# Creating class
class Student:
    name = "Prince Raj"


# creating object(instance)
s1 = Student()
print(s1.name)


class Car:
    name = "Tata"
    Brand = "Mercedes"


car1 = Car()
print(car1.name)
print(car1.Brand)
