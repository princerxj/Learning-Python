# Tuples
# A built in datatype that lets us create immutable sequence of values
# Example : tup = (87, 64, 33, 95, 76)
# tup[0] = 43 , Not allowed
# Slicing in tuples works same as of strings and lists
tup = (2, 1, 3, 1)
print(tup.index(1))  # returns index of first occurence
print(tup.count(1))  # count total occurences
